    public void llenarEspecialidad(){
        ControladorDoctor doctor = new ControladorDoctor(); 
        ArrayList<ModeloEspecialidad> listaEspecialidad =    doctor.getEspecialidad(); 
        jComboBoxEspecialida.removeAllItems();
        for (int i = 0; i < listaEspecialidad.size(); i++){
            jComboBoxEspecialida.addItem(new ModeloEspecialidad(listaEspecialidad.get(i).getIdEspecialidad(), listaEspecialidad.get(i).getEspecialidad()));
        }  
    }
