    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
      boolean estado; 
        try{
        DoctorModelo doctor = new DoctorModelo(); 
        doctor.setId(Integer.parseInt(jTextField7.getText()));
       doctor.setNombreDoctor(jTextField1.getText());
       doctor.setApellidoDoctor(jTextField2.getText());
       doctor.setTelefonoDoctor(jTextField3.getText());
       doctor.setColegiadoDoctor(jTextField4.getText());
       doctor.setEmailDoctor(jTextField5.getText());
       if (jCheckBox2.isSelected() ){
           estado = true;
       }else{
           estado = false; 
       }
       doctor.setEstadoDoctor(estado);
       doctor.setIdEspecialidad(jComboBoxEspecialida.getItemAt(jComboBoxEspecialida.getSelectedIndex()).getIdEspecialidad());
       String direccion = jTextField8.getText();
       int muni = jComboBoxMuni.getItemAt(jComboBoxMuni.getSelectedIndex()).getIdMunicipio();
       int dept  = jComboBoxDep.getItemAt(jComboBoxDep.getSelectedIndex()).getIdDepartamento();
       
        ControladorDoctor controladorDoctor = new ControladorDoctor(); 
        controladorDoctor.actualizarDoc(doctor, direccion, muni, dept);
        }catch (NumberFormatException ex) {
           
        }finally {
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
            jTextField8.setText("");
            jTextField7.setText("");
            
        }
        
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

